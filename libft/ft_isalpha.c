#include "libft.h"

int	ft_isalpha(int c)
{
	if ((c >= 'a') && (c <= 'z'))
	{
		return (1024);
	}
	if ((c >= 'A') && (c <= 'Z'))
	{
		return (1024);
	}
	return (0);
}
